import mongoose from 'mongoose';

const ForumPostSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    content: {
        type: String,
        required: true,
        trim: true
    },
    subject: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Subject',
        required: true
    },
    author: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    isApproved: {
        type: Boolean,
        default: false
    },
    commentsCount: {
        type: Number,
        default: 0
    }
}, {
    timestamps: true 
});

export default mongoose.model('ForumPost', ForumPostSchema);
